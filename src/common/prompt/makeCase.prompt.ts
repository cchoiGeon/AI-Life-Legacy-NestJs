export function createCasePrompt(data: string) {
  return `
    사용자의 답변을 통해 케이스를 분류합니다.
    사용자 데이터: ${data}
    케이스 분류 기준:
    case1: 대학교를 다님, 결혼을 함, 자녀가 있음
    case2: 대학교를 다님, 결혼을 함, 자녀가 없음
    case3: 대학교를 다님, 결혼을 하지 않음, 자녀가 없음
    case4: 대학교를 다니지 않음, 결혼을 함, 자녀가 있음
    case5: 대학교를 다니지 않음, 결혼을 함, 자녀가 없음
    case6: 대학교를 다니지 않음, 결혼을 하지 않음, 자녀가 없음

    결혼 상태는 '기혼' 또는 '미혼'으로 구분합니다.
    대학교 여부는 대학을 다닌 경우로 간주합니다.

    일치하는 case 번호를 'case번호' 형태로만 응답하세요. 예를 들어, case1이라면 'case1'로 응답하세요. 따옴표나 추가적인 설명은 포함하지 마세요.
    `;
}
